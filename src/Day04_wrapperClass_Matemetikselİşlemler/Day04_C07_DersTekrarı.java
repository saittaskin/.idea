package Day04_wrapperClass_Matemetikselİşlemler;

public class Day04_C07_DersTekrarı {

    public static void main(String[] args) {

        int sayi = 10;

        System.out.println(sayi++); // 10
        System.out.println(sayi); // 11

        int sayi1 = 10;

        System.out.println(++sayi1); // 11
        System.out.println(sayi1); // 11
    }
}
